// Binary search lookup function
static bool is_word_in_compact_dictionary(const char* word) {
    if (!word) return false;

    int left = 0;
    int right = COMPACT_DICT_SIZE - 1;

    while (left <= right) {
        int mid = left + (right - left) / 2;
        int cmp = strcmp(word, compact_dictionary[mid]);

        if (cmp == 0) {
            return true;
        } else if (cmp < 0) {
            right = mid - 1;
        } else {
            left = mid + 1;
        }
    }

    return false;
}

